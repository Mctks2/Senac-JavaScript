// Arrow function que multiplica dois números (Função Anônima / Função de seta)
const multiplicacao = (a, b) => {
    return a * b;}

// Chamando a arrow function e armazenando o resultado em uma variável
let resultadoMultiplicacao = multiplicacao(4, 6);
console.log("O resultado da multiplicação é: " +resultadoMultiplicacao);